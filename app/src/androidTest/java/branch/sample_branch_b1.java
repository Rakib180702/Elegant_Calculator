package branch;

public class sample_branch_b1
{

    String name, id;

    float cgpa;
    public sample_branch_b1(String a, String name, String id, float cgpa) {
        this.a = a;
        this.name = name;
        this.id = id;
        this.cgpa = cgpa;
    }

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public float getCgpa() {
        return cgpa;
    }

    public void setCgpa(float cgpa) {
        this.cgpa = cgpa;
    }
}